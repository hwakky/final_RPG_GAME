package entity;

public class ElementPosition {

    private int elemX;
    private int elemY;

    public ElementPosition(int elemX, int elemY) {
        this.elemX = elemX;
        this.elemY = elemY;
    }

    public int getElemX() {
        return elemX;
    }

    public void setElemX(int elemX) {
        this.elemX = elemX;
    }

    public int getElemY() {
        return elemY;
    }

    public void setElemY(int elemY) {
        this.elemY = elemY;
    }

}
